    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "No row selected..!!!\n Please select a row to delete.", "Warning Alert", JOptionPane.WARNING_MESSAGE);
        } else {
            confirm = new JFrame("confirm");
            if (JOptionPane.showConfirmDialog(this, "Confirm that you want to Delete", "Sacco Management System", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
                int index = jTable1.getSelectedRow();
                String id = jTable1.getValueAt(index, 0).toString();

                if (delete(id)) {
                    JOptionPane.showMessageDialog(null, "Member deleted successifully...");
                    retreave();
                } else {
                    JOptionPane.showMessageDialog(null, "Data not deleted");
                }
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

