    private void retreave() {
        try {
            String sql = "SELECT * FROM customers";
            con = DriverManager.getConnection("jdbc:mysql://localhost/sacco", "root", "");
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();

            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                String id = rs.getString(1);
                String Name = rs.getString(2);
                String Email = rs.getString(11);
                String Account = rs.getString(8);
                String kra = rs.getString(4);
                String phone = rs.getString(7);

                model.addRow(new String[]{id, Name, Account, kra, phone, Email});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    public boolean delete(String id) {
        try {
            String sql = "DELETE FROM customers WHERE id= '" + id + "' ";

            con = DriverManager.getConnection("jdbc:mysql://localhost/sacco", "root", "");
            pst = con.prepareStatement(sql);
            pst.execute(sql);
            return true;
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
            return false;
        }
    }

    private void filter(String query1) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model);
        jTable1.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter(query1));
    }

